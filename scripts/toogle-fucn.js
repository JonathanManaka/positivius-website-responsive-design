$(document).ready(function(){
  $("#toogle-div").toggle();
  $("#step1-id-container").css("background-color", "#FFFFFF")
    $("#show-hide-btn").click(function(){
       $("#toogle-div").toggle();
       if($("#toogle-div").is(":hidden")){
          $("#step1-id-container").css("background-color", "#FFFFFF")
          $("#toggle-icon").attr("src","assets/icons/plus-icon.svg")
       }else{
            $("#step1-id-container").css("background-color", "#B9FF66")
            $("#toggle-icon").attr("src","assets/icons/minus-icon.svg")
       }
    });
  });

